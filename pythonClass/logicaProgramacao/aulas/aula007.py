#Operadores Aritiméticos

adicao = 10 + 10
subtracao = 10 - 8
multiplicacao = 10 * 10
divisao = 25 / 5 #Sempre retorna float
modulo = 25 % 5
modulo2 = 25 % 3

print(f"Adicao {adicao}")
print(f"Subtracao {subtracao}")
print(f"Multiplicacao {multiplicacao}")
print(f"Divisao {divisao}")
print(f"Modulo 1 =  {modulo}")
print(f"Modulo 2 = {modulo2}")

divisao_comum = 10 / 3
divisao_inteira = 10 // 3

print(f"Divisão Comum {divisao_comum}")
print(f"Divisão Inteira {divisao_inteira}")
#A divisão inteira sempre irá corta os números flutuantes do resultado.


potencia = 10 ** 3

print(f"Exponenciação {potencia}")





